#include "../../include/minirt.h"

int		get_index(char *line, int i)
{
	int j;

	j = i;
	while ((line[j] >= '0' && line[j] <= '9') ||
			line[j] == '.' || line[j] == ',' || line[j] == '-')
		j++;
	if (line[j] != ' ' && line[j] != '\0')
		errormsg(5);
	return (j);
}
